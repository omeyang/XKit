// Package xproc 提供当前进程信息查询工具。
//
// # 功能概览
//
//   - [ProcessID]: 获取当前进程 PID
//   - [ProcessName]: 获取当前进程名称（不含路径），首次调用后缓存结果
//
// # 典型用途
//
// 进程标识信息常用于日志结构化字段、指标标签、分布式追踪的 service.name 属性等
// "尽力获取"场景，为可观测性数据提供进程级上下文。
//
// # 设计决策: 独立成包
//
// xproc 关注进程标识信息（PID、进程名），与 xsys 的系统资源限制管理（文件描述符上限）
// 属于不同职责域。Go 惯用法推荐按职责边界划分包，即使包体量较小，
// 清晰的职责划分也优于将不相关功能合并到同一个包中。
package xproc
